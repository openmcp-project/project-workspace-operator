//+kubebuilder:webhook:path=/validate-core-openmcp-cloud-v1alpha1-project,mutating=false,failurePolicy=fail,sideEffects=None,groups=core.openmcp.cloud,resources=projects,verbs=create;update;delete,versions=v1alpha1,name=vproject.kb.io,admissionReviewVersions=v1

var _ webhook.CustomValidator = &projectValidator{}

type projectValidator struct {
	client.Client
	overrideName string
}

// ValidateCreate implements webhook.CustomValidator so a webhook will be registered for the type
func (v *projectValidator) ValidateCreate(ctx context.Context, obj runtime.Object) (warnings admission.Warnings, err error) {
	project, err := expectProject(obj)
	if err != nil {
		return
	}
	projectlog.Info("validate create", "name", project.Name)

	userInfo, err := userInfoFromContext(ctx)
	if err != nil {
		return
	}

	validRole, err := v.ensureValidRole(ctx, project)
	if err != nil {
		return warnings, err
	}
	if !validRole {
		return warnings, errRequestingUserNoAccess(userInfo.Username)
	}

	return
}

// ValidateUpdate implements webhook.CustomValidator so a webhook will be registered for the type
func (v *projectValidator) ValidateUpdate(ctx context.Context, oldObj, newObj runtime.Object) (warnings admission.Warnings, err error) {
	oldProject, err := expectProject(oldObj)
	if err != nil {
		return
	}

	newProject, err := expectProject(newObj)
	if err != nil {
		return
	}
	projectlog.Info("validate update", "name", oldProject.Name)

	if err = verifyCreatedByUnchanged(oldProject, newProject); err != nil {
		return
	}

	userInfo, err := userInfoFromContext(ctx)
	if err != nil {
		return
	}
	validRole, err := v.ensureValidRole(ctx, oldProject)
	if err != nil {
		return warnings, err
	}
	if !validRole {
		return warnings, errRequestingUserNoAccess(userInfo.Username)
	}

	// ensure the user can't remove themselves from the member list
	validNewRole, err := v.ensureValidRole(ctx, newProject)
	if err != nil {
		return warnings, err
	}
	if !validNewRole {
		return warnings, errRequestingUserNoAccess(userInfo.Username)
	}

	return
}

// ValidateDelete implements webhook.CustomValidator so a webhook will be registered for the type
func (v *projectValidator) ValidateDelete(ctx context.Context, obj runtime.Object) (warnings admission.Warnings, err error) {
	project, err := expectProject(obj)
	if err != nil {
		return
	}
	projectlog.Info("validate delete", "name", project.Name)

	if validRole, err := v.ensureValidRole(ctx, project); !validRole {
		return warnings, err
	}
	return
}

// expectProject casts the given runtime.Object to *Project. Returns an error in case the object can't be casted.
func expectProject(obj runtime.Object) (*Project, error) {
	project, ok := obj.(*Project)
	if !ok {
		return nil, fmt.Errorf("expected a Project but got a %T", obj)
	}
	return project, nil
}

func (v *projectValidator) ensureValidRole(ctx context.Context, project *Project) (bool, error) {
	userInfo, err := userInfoFromContext(ctx)
	if err != nil {
		return false, fmt.Errorf("failed to get userInfo")
	}
	if project.UserInfoHasRole(userInfo, ProjectRoleAdmin) || isOwnServiceAccount(userInfo) {
		return true, nil
	}

	if v.overrideName == "" {
		return false, nil
	}

	overrides := &MemberOverrides{}
	if err := v.Get(ctx, types.NamespacedName{Name: v.overrideName}, overrides); err != nil {
		return false, err
	}
	if overrides.HasAdminOverrideForResource(&userInfo, project.Name, project.Kind) {
		return true, nil
	}
	return false, nil
}
